#!/usr/bin/env node
/*
 * build-panorama-catalog.js
 *
 * Scans every scene's script_general.js for panorama IDs,
 * pulls friendly labels from locale/en.txt,
 * and writes /panorama-catalog.js — which the admin UI
 * reads to populate the Panorama dropdown.
 *
 * Re-run any time you add a new scene folder or panorama.
 *   Usage:  node tools/build-panorama-catalog.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SCENES_ROOT = path.join(ROOT, 'pilgrimspath-vr', 'pilgrims path main');

if (!fs.existsSync(SCENES_ROOT)) {
  console.error('Scenes root not found:', SCENES_ROOT);
  process.exit(1);
}

const PANO_RE  = /(panorama_[A-F0-9]+_[A-F0-9]+_[A-F0-9]+_[A-F0-9]+_[A-F0-9]+)/g;
const LABEL_RE = /(panorama_[A-F0-9_]+)\.label\s*=\s*(.+?)\r?\n/g;

const sceneFolders = fs.readdirSync(SCENES_ROOT)
  .filter(n => fs.statSync(path.join(SCENES_ROOT, n)).isDirectory())
  .filter(n => !/\.(backup|bak|old)$/i.test(n));

const byFolder = {};
for (const folder of sceneFolders) {
  const sg = path.join(SCENES_ROOT, folder, 'script_general.js');
  if (!fs.existsSync(sg)) continue;
  const txt = fs.readFileSync(sg, 'utf8');
  const ids = new Set();
  let m; while ((m = PANO_RE.exec(txt))) ids.add(m[1]);

  const labels = {};
  const loc = path.join(SCENES_ROOT, folder, 'locale', 'en.txt');
  if (fs.existsSync(loc)) {
    const lt = fs.readFileSync(loc, 'utf8');
    let n; while ((n = LABEL_RE.exec(lt))) labels[n[1]] = n[2].trim();
  }

  byFolder[folder] = Array.from(ids).map(id => ({ id, label: labels[id] || '' }))
    .sort((a, b) => (a.label || a.id).localeCompare(b.label || b.id));
}

/* Map admin scene-keys to scene folders.
 * Some keys share a folder (e.g. tawaf-initial / tawaf-ifadha / tawaf-farewell
 * all live in "1 Tawaf") and that's fine — they all see the same panoramas.
 */
const SCENE_KEY_TO_FOLDER = {
  'tawaf-initial':  '1 Tawaf',
  'tawaf-ifadha':   '1 Tawaf',
  'tawaf-farewell': '1 Tawaf',
  'safa-marwa':     '2 Safa and Marwa',
  'mina-8th':       '3 Mina',
  'mina-tents-12':  '3 Mina',
  'arafah-9th':     '4 Arafah',
  'muzdalifah-9th': 'Muzdalifah',
  'jamarat-10th':   'Jamarat Aqabah',
  'jamarat-11th':   'Jamarat Base 2',
  'jamarat-12th':   'Jamarat Base 2',
  // Scenes without panoramas (HTML-only) — admin will show "no panoramas":
  'barber-umrah':   null,
  'rest-umrah':     null,
  'reenter-ihram':  null,
  'qurbani-10th':   null,
  'barber-hajj':    null
};

const out =
  '/* AUTO-GENERATED by tools/build-panorama-catalog.js — do not edit by hand.\n' +
  '   Regenerate with:  node tools/build-panorama-catalog.js                    */\n' +
  '(function(){\n' +
  '  var BY_FOLDER = ' + JSON.stringify(byFolder, null, 2) + ';\n' +
  '  var SCENE_KEY_TO_FOLDER = ' + JSON.stringify(SCENE_KEY_TO_FOLDER, null, 2) + ';\n' +
  '  window.PPPanoramaCatalog = {\n' +
  '    forFolder: function(folder){ return (BY_FOLDER[folder]||[]).slice(); },\n' +
  '    forSceneKey: function(key){\n' +
  '      var f = SCENE_KEY_TO_FOLDER[key];\n' +
  '      return f ? (BY_FOLDER[f]||[]).slice() : [];\n' +
  '    },\n' +
  '    folderForSceneKey: function(key){ return SCENE_KEY_TO_FOLDER[key] || null; },\n' +
  '    label: function(folder, id){\n' +
  '      var p = (BY_FOLDER[folder]||[]).find(function(x){return x.id===id;});\n' +
  '      return p ? p.label : "";\n' +
  '    },\n' +
  '    folders: function(){ return Object.keys(BY_FOLDER); }\n' +
  '  };\n' +
  '})();\n';

fs.writeFileSync(path.join(ROOT, 'panorama-catalog.js'), out, 'utf8');

let total = 0;
for (const k of Object.keys(byFolder)) {
  console.log('  ' + k + ': ' + byFolder[k].length + ' panoramas');
  total += byFolder[k].length;
}
console.log('Wrote panorama-catalog.js  (' + total + ' panoramas across ' + Object.keys(byFolder).length + ' folders)');
