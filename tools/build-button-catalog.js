#!/usr/bin/env node
/*
 * build-button-catalog.js
 *
 * Scans every scene's HTML for <button> / <a role="button"> elements
 * and writes /button-catalog.js — admin UI uses this to populate the
 * "Button" dropdown when adding a button-trigger banner, so you never
 * have to guess button text or invent an id.
 *
 * Re-run after editing scene HTML.
 *   Usage:  node tools/build-button-catalog.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SCENES_ROOT = path.join(ROOT, 'pilgrimspath-vr', 'pilgrims path main');

// Map admin scene-keys to scene file paths (must match admin DEFAULT_DATA `file:` field)
const SCENE_KEY_TO_FILE = {
  'tawaf-initial':  '1 Tawaf/index.htm',
  'tawaf-ifadha':   '1 Tawaf/index.htm',
  'tawaf-farewell': '1 Tawaf/index.htm',
  'safa-marwa':     '2 Safa and Marwa/index.htm',
  'mina-8th':       '3 Mina/index.htm',
  'mina-tents-12':  '3 Mina/index.htm',
  'arafah-9th':     '4 Arafah/index.htm',
  'muzdalifah-9th': 'Muzdalifah/index.htm',
  'jamarat-10th':   'Jamarat Aqabah/index.htm',
  'jamarat-11th':   'Jamarat Base 2/index.htm',
  'jamarat-12th':   'Jamarat Base 2/index.htm',
  'barber-umrah':   '5 Rami Jamarat, Qurbani, trim Shave, Tawaf/barber-scene.html',
  'qurbani-10th':   '5 Rami Jamarat, Qurbani, trim Shave, Tawaf/qurbani-scene.html',
  'barber-hajj':    '5 Rami Jamarat, Qurbani, trim Shave, Tawaf/barber-scene.html',
  'rest-umrah':     '0 Ihram/rest-scene.html',
  'reenter-ihram':  '0 Ihram/ihram-scene.html'
};

// Match <button ...>text</button>  and  <a ... role="button">text</a>
const BTN_RE = /<(button|a)\b([^>]*)>([\s\S]*?)<\/\1>/gi;

function attr(tag, name){
  const re = new RegExp('\\b'+name+'\\s*=\\s*"([^"]*)"', 'i');
  const m = tag.match(re); return m ? m[1] : '';
}

function stripTags(s){ return s.replace(/<[^>]+>/g,' ').replace(/\s+/g,' ').trim(); }

function scanFile(file){
  if (!fs.existsSync(file)) return [];
  const html = fs.readFileSync(file, 'utf8');
  const out = [];
  const seen = new Set();
  let m;
  while ((m = BTN_RE.exec(html))) {
    const attrs = m[2] || '';
    const text  = stripTags(m[3] || '');
    const id    = attr(attrs, 'id');
    const cls   = attr(attrs, 'class');
    const onc   = attr(attrs, 'onclick');
    const role  = attr(attrs, 'role');
    if (m[1].toLowerCase()==='a' && role!=='button') continue;
    if (!text && !id) continue;
    const key = (id || '') + '|' + text;
    if (seen.has(key)) continue;
    seen.add(key);
    out.push({ id, text, cls, onclick: onc });
  }
  return out;
}

const byScene = {};
for (const [sceneKey, rel] of Object.entries(SCENE_KEY_TO_FILE)) {
  const file = path.join(SCENES_ROOT, rel);
  byScene[sceneKey] = scanFile(file);
}

const out =
  '/* AUTO-GENERATED by tools/build-button-catalog.js — do not edit by hand.\n' +
  '   Regenerate with:  node tools/build-button-catalog.js                  */\n' +
  '(function(){\n' +
  '  var BY_SCENE = ' + JSON.stringify(byScene, null, 2) + ';\n' +
  '  window.PPButtonCatalog = {\n' +
  '    forSceneKey: function(key){ return (BY_SCENE[key]||[]).slice(); }\n' +
  '  };\n' +
  '})();\n';

fs.writeFileSync(path.join(ROOT, 'button-catalog.js'), out, 'utf8');

let total = 0;
for (const k of Object.keys(byScene)) {
  console.log('  ' + k + ': ' + byScene[k].length + ' buttons');
  total += byScene[k].length;
}
console.log('Wrote button-catalog.js  (' + total + ' buttons across ' + Object.keys(byScene).length + ' scenes)');
