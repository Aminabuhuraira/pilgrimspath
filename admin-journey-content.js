/* ─── Languages ─── */
function addLanguagePrompt(){
  var code = prompt('Language code (e.g. fr, ur, tr):');
  if(!code) return;
  code = code.trim().toLowerCase();
  if(data.languages.find(function(l){return l.code===code;})){ alert('Language already exists.'); return; }
  var name = prompt('Display name (e.g. Fran\u00e7ais, \u0627\u0631\u062f\u0648, T\u00fcrkçe):', code.toUpperCase()) || code.toUpperCase();
  var defFolder = ({fr:'French/',ur:'Urdu/',tr:'Turkish/',id:'Indonesian/',ms:'Malay/',es:'Spanish/',fa:'Persian/',ar:'Arabic/'})[code] || (name.replace(/[^A-Za-z]/g,'')+'/');
  var folder = prompt('Subfolder under /hajj voiceover english/ where this language\u2019s audio files live:\n\nDrop your translated MP3s into that folder using the SAME filenames as English (1.mp3, 2.mp3, \u2026 or the descriptive names shown in the editor). Pilgrims who pick this language will hear them automatically.', defFolder) || defFolder;
  if(folder && !/\/$/.test(folder)) folder += '/';
  var dir = /^(ar|ur|fa|he)$/i.test(code) ? 'rtl' : 'ltr';
  data.languages.push({code:code,name:name.trim(),dir:dir,folder:folder,isDefault:false});
  // Auto-seed with English filenames so admins only need to drop the translated MP3s into the folder
  data.scenes.forEach(function(s){
    s.banners.forEach(function(b){
      var enFile = b.audio && b.audio.en;
      var enChain = b.audioChain && b.audioChain.en;
      var enText = b.text && b.text.en;
      if(enFile){ b.audio = b.audio||{}; b.audio[code] = enFile; }
      if(enChain){ b.audioChain = b.audioChain||{}; b.audioChain[code] = enChain; }
      if(enText){ b.text = b.text||{}; b.text[code] = {title:enText.title||'',body:enText.body||''}; }
    });
  });
  markDirty(); renderLangBar(); renderMain();
  flash('Added '+name+' \u2014 audio files expected in /hajj voiceover english/'+folder);
}
function removeLanguage(code){
  if(!confirm('Remove "'+code+'" and all its translations?')) return;
  data.languages = data.languages.filter(function(l){return l.code!==code;});
  data.scenes.forEach(function(s){ s.banners.forEach(function(b){
    if(b.text)delete b.text[code]; if(b.audio)delete b.audio[code]; if(b.audioChain)delete b.audioChain[code];
  }); });
  if(activeLang===code) activeLang = data.languages[0].code;
  markDirty(); renderLangBar(); renderMain();
}

/* ─── Audio: upload + preview ─── */
function uploadAudioFor(bannerId, field){
  var inp = document.createElement('input');
  inp.type='file'; inp.accept='audio/*';
  inp.onchange=function(){
    var f=inp.files[0]; if(!f) return;
    if(f.size > 6*1024*1024){ if(!confirm('File is '+(f.size/1024/1024).toFixed(1)+' MB. Storing as base64 in localStorage may exceed quota. Continue?')) return; }
    var r=new FileReader();
    r.onload=function(){
      var sc=findScene(activeSceneKey); var b=findBanner(sc,bannerId); if(!b) return;
      b[field] = b[field]||{};
      b[field][activeLang] = r.result;
      markDirty(); renderEditor();
      flash('Audio uploaded — remember to Save');
    };
    r.readAsDataURL(f);
  };
  inp.click();
}
function clearAudioFor(bannerId, field){
  var sc=findScene(activeSceneKey); var b=findBanner(sc,bannerId); if(!b) return;
  var current = (b[field]&&b[field][activeLang])||'';
  if(!current) return;
  if(!confirm('Remove the '+(field==='audioChain'?'chained ':'')+'audio file for ['+activeLang+']? Banner text is kept.')) return;
  b[field] = b[field]||{};
  b[field][activeLang] = '';
  markDirty(); save(); renderEditor();
  flash('Audio file removed');
}
